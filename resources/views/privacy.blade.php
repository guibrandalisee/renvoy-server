    <h1>Privacy Policy</h1>
    <p class="lead">Renvoy is a private, local-first subscription tracker. It does not require an account, sell personal data, or upload your subscription data.</p>

    <h2>Data stored on your device</h2>
    <p>Subscription names, prices, dates, groups, notes, payment labels, and settings stay in the app's local database, protected by your device's security. Renvoy does not upload this information for currency conversion.</p>

    <h2>Network services</h2>
    <p>Renvoy requests a public service catalog from renvoy.g22.dev and daily reference exchange rates from Frankfurter. Currency requests contain currency codes only, never subscription names or prices. These services and their infrastructure may process basic network metadata such as your IP address.</p>

    <h2>Notifications</h2>
    <p>Renewal reminders are created and scheduled on your device. Notification permission is optional and can be changed in system settings.</p>

    <h2>Backups and exports</h2>
    <p>Backup and CSV files leave the app only when you explicitly export and share them. You control the destination and are responsible for copies stored outside Renvoy.</p>

    <h2>Control and deletion</h2>
    <p>You can delete subscriptions and groups in the app. Uninstalling Renvoy removes its local database, subject to your device's backup and restore settings.</p>

    <h2>Contact</h2>
    <p>For privacy questions, contact the support address listed on Renvoy's App Store or Google Play page.</p>

